// Basic Math

let num1 = 100;
console.log(num1 + 100);
console.log(num1 - 10);
console.log(num1 * 10);
console.log(num1 / 10);
console.log(num1 % 1500);  // Remainder
console.log(++num1);  // Increment
console.log(--num1);  // Decrement
console.log(Math.PI); // PI
console.log(Math.sqrt(num1)); // Square a root